    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed

        this.usuario = jTextUsuario.getText();
        this.contrasena = txtContrasena.getText();

        if (!this.usuario.equals("") || !this.contrasena.equals("")) {

            try {

                PreparedStatement ps = cn.prepareStatement("SELECT * FROM usuario WHERE usuario='" +this.usuario+ " ' AND contrasena='"+this.contrasena+ "'");
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {

                    String nivel = rs.getString("tipo_usuario");

                    if (nivel.equalsIgnoreCase("Empleado")) {
                        dispose();
                        new frmfactura().setVisible(true);

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Error al iniciar sesion");
                    this.jTextUsuario.setText("");
                    this.txtContrasena.setText("");
                }

            } catch (Exception e) {

            }

        } else {

            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios");
        }

    }//GEN-LAST:event_btnIniciarActionPerformed

