    private void btnRegistrarPOSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPOSActionPerformed
      
        String Nombre = this.txtNombre.getText();
        int Precio = Integer.parseInt(this.txtPrecio.getText());
        int Cantidad = Integer.parseInt(this.txtCantidad.getText());
        Postre postresito = new Postre(Nombre, Precio, Cantidad);
        
        if ( btnRegistrarPOS.getText() == "Agregar"  || btnRegistrarPOS.getText() == "Add") {
            postresitos.add(postresito);
            
             
            try {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO postre (nombre,precio,cantidad,estado) VALUES (?,?,?,?)");
                ps.setString(1, Nombre);
                ps.setInt(2, Precio);
                ps.setInt(3, Cantidad);
                ps.setString(4, "A");
                ps.executeUpdate();
                JOptionPane.showMessageDialog( null , "Postre Registrado ");
                
            } catch (SQLException ex) {
               System.err.println(ex);
               JOptionPane.showMessageDialog(null, "Error al registrar el plato");
            }
        } 
        else if (btnRegistrarPOS.getText() == "Modificar" ) {
           // postresitos.set(this.modificarRegistroPOS, postresito);
           
            try {

                PreparedStatement ps = cn.prepareStatement("UPDATE postre SET nombre= '" + Nombre + "', precio= " + Precio + ", cantidad= " + Cantidad + " WHERE id=" + postresitos.get(modificarRegistroPOS).id);
                int respuesta = ps.executeUpdate();

                if (respuesta > 0) {
                    JOptionPane.showMessageDialog(null, "ActualizaciÃ³n exitosa");
                }

            } catch (SQLException e) {

                System.err.println("Error al actualizar " + e);

            }
        }
        LimpiarCampos();
        
    }//GEN-LAST:event_btnRegistrarPOSActionPerformed

