    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        String Nombre = this.txtNombre.getText();
        int Precio = Integer.parseInt(this.txtPrecio.getText());
        int Cantidad = Integer.parseInt(this.txtCantidad.getText());
        Jugo juguito = new Jugo(Nombre, Precio, Cantidad);
        
        if (btnRegistrar.getText() == "Agregar" || btnRegistrar.getText() == "Add") {
        juguitos.add(juguito);
        
         
            try {
                PreparedStatement ps = cn.prepareStatement("INSERT INTO jugo (nombre,precio,cantidad,estado) VALUES (?,?,?,?)");
                ps.setString(1, Nombre);
                ps.setInt(2, Precio);
                ps.setInt(3, Cantidad);
                ps.setString(4, "A");
                ps.executeUpdate();
                JOptionPane.showMessageDialog( null , "Jugo Registrado ");
                
            } catch (SQLException ex) {
               System.err.println(ex);
               JOptionPane.showMessageDialog(null, "Error al registrar el plato");
            }
        
        } else if (btnRegistrar.getText() == "Modificar") {
           // juguitos.set(this.registroModificarJ, juguito);
           
            try {

                PreparedStatement ps = cn.prepareStatement("UPDATE jugo SET nombre= '" + Nombre + "', precio= " + Precio + ", cantidad= " + Cantidad + " WHERE id=" + juguitos.get(registroModificarJ).id);
                int respuesta = ps.executeUpdate();

                if (respuesta > 0) {
                    JOptionPane.showMessageDialog(null, "ActualizaciÃ³n exitosa");
                }

            } catch (SQLException e) {

                System.err.println("Error al actualizar " + e);

            }
        }
       LimpiarCampos();
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

